/**
 * Obtient un duah rabanah aléatoire
 */
export function getRandomDuahRabanah(): DuahRabanah {
  const randomIndex = Math.floor(Math.random() * DUAH_RABANAH.length);
  return DUAH_RABANAH[randomIndex];
}

/**
 * Obtient un duah rabanah par index (pour rotation sur 24h)
 */
export function getDuahRabanahByIndex(index: number): DuahRabanah {
  const normalizedIndex = index % DUAH_RABANAH.length;
  return DUAH_RABANAH[normalizedIndex];
}


